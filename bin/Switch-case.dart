void main(){
  var size = 'L';

  switch(size){
    case 'XS':
      print('my shirt size is xs');
      break;
    case 'S':
      print('my shirt size is S');
      break;
    case 'M':
      print('my shirt size is M');
      break;
    case 'L':
      print('my shirt size is L');
      break;
    case 'XL':
      print('my shirt size is XL');
      break;
    default:
      print('none of the above');
  }
}