void main(){
  int x = 2; // 0010
  int y = 7; // 0111

  ///  x &  y =0010
  ///   x |  y =0111
  ///   x ^ y  =0101
   print(x & y);
   print(x | y);
   print(x ^ y);

   int z = 7 ; // 0111

  print(z >> 2); // 0000 0111 >> 2  -> 0000 01   -> 0000 0001 = 1
  print(z << 1); // 0000 0111 << 1  -> 000 0111  -> 0000 1110 = 14

}